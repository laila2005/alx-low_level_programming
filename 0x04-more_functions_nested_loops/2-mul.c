#include "main.h"

/**
 * mul - Multiplies two integers
 * @a: The first integer operand
 * @b: The second integer operand
 *
 * Description: This function takes two integers, 'a' and 'b',
 * and computes their product using the multiplication operator '*'.
 * The result of the multiplication is returned as an integer.
 *
 * Return: The product of 'a' and 'b'
 */

int mul(int a, int b)
{
int multi = (a * b);
return (multi);
}
