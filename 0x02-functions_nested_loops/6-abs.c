#include "main.h"

/**
 * _abs - computes the absolute value of an integer
 * @n: the integer to compute
 *
 * Description: This function takes an integer `n` and returns its absolute
 * value. If `n` is negative, it returns `-n`, otherwise, it returns `n`.
 *
 * Return: The absolute value of the integer `n`
 */

int _abs(int n)
{
if (n < 0)
{
return (-n); }
else
{
return (n); }
}
